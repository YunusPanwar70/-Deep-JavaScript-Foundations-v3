// Boolean
function BooleanValue(check) {
    return typeof check
};

console.log(BooleanValue(true));

// Number

function number(num) {
    return typeof num
}

console.log(number(4));

// Null

function nullValue(val) {
    return typeof val
};

console.log(nullValue(null));

// String

function stringValue(str) {
    return typeof str;
};

console.log(stringValue('40'));

// Symbol 

function symbolValue(s) {
    return typeof s;
};

console.log(symbolValue(Symbol()));

// Undefined

function unvalue(find) {
    return typeof find
};

console.log(unvalue());